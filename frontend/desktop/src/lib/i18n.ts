export function getLocale(): Locale {
  const raw = localStorage.getItem(I18N_KEY);
  return raw === "en-US" ? "en-US" : "zh-CN";
}

export function setLocale(locale: Locale): void {
  localStorage.setItem(I18N_KEY, locale);
  window.dispatchEvent(new CustomEvent("locale_changed", { detail: { locale } }));
}

export function t(key: string, locale?: Locale): string;
export function t(key: string, params: Record<string, string | number>, locale?: Locale): string;
export function t(key: string, paramsOrLocale?: Record<string, string | number> | Locale, locale?: Locale): string {
  const lang = typeof paramsOrLocale === "string" ? paramsOrLocale : (locale || getLocale());
  const params = typeof paramsOrLocale === "object" && paramsOrLocale !== null ? paramsOrLocale : undefined;
  let out = MESSAGES[lang]?.[key] || MESSAGES["zh-CN"]?.[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) out = out.replace(new RegExp(`\\{\\{${k}\\}\\}`, "g"), String(v));
  }
  return out;
}
