            additional_kwargs={
                "ui": [
                    {
                        "type": "file_generated",
                        "target": "chat",
                        "file_path": result["file"],
                        "file_type": "docx",
                        "metadata": {
                            "paragraphs": result["paragraphs"],
                            "title": title,
                        }
                    },
                    {
                        "type": "editor_action",
                        "action": "open",
                        "target": "editor",
                        "file_path": result["file"],
                        "auto_trigger": True,
                    }
                ]
            }
