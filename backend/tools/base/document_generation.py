def _resolve_slide_image_path(item: Dict[str, Any], output_dir: str) -> Optional[str]:
    """解析单页幻灯片的图片：支持 image_path（本地）或 image_url（下载到 output_dir 后返回路径）。"""
    image_path = (item.get("image_path") or item.get("image_url") or "").strip()
    if not image_path:
        return None
    if image_path.startswith(("http://", "https://")):
        try:
            import urllib.request
            from urllib.parse import urlparse
            name = Path(urlparse(image_path).path).name or "image"
            if not name.lower().endswith((".png", ".jpg", ".jpeg", ".gif", ".webp")):
                name += ".png"
            local_path = str(Path(output_dir) / f"slide_img_{abs(hash(image_path)) % 10**8}_{name}")
            urllib.request.urlretrieve(image_path, local_path)
            return local_path
        except Exception as e:
            logger.warning("PPT 幻灯片图片下载失败 %s: %s", image_path[:80], e)
            return None
    p = Path(image_path)
    if not p.is_absolute():
        p = (get_workspace_root() / p).resolve()
    return str(p) if p.exists() else None


async def _generate_ppt_internal(
    title: str,
    content: List[Dict[str, Any]],
    output_path: str = "output.pptx",
) -> Dict[str, Any]:
    """
    内部实现：生成 PowerPoint 文档
    
    Args:
        title: 演示文稿标题
        content: 幻灯片内容列表
                 格式: [{"heading": "...", "text": "...", "image_path": "可选", "image_url": "可选"}, ...]
        output_path: 输出文件路径
    
    Returns:
        生成结果
    """
    output_path = _resolve_output_path(output_path)
    output_dir = str(Path(output_path).parent)
    try:
        # ✅ 检查依赖是否安装
        try:
            from pptx import Presentation
            from pptx.util import Inches, Pt
        except ImportError as import_err:
            logger.warning(f"⚠️ python-pptx 未安装: {import_err}")
            return {
                "status": "error",
                "error": f"python-pptx 模块未安装。请运行: pip install python-pptx",
                "message": f"PPT 生成失败: python-pptx 模块未安装。请运行: pip install python-pptx"
            }
        
        # 创建演示文稿
        prs = Presentation()
        prs.slide_width = Inches(10)
        prs.slide_height = Inches(7.5)
        
        # 添加标题幻灯片
        title_slide_layout = prs.slide_layouts[0]
        slide = prs.slides.add_slide(title_slide_layout)
        title_shape = slide.shapes.title
        subtitle = slide.placeholders[1]
        title_shape.text = title
        subtitle.text = "Generated by Document Agent"
        
        # 添加内容幻灯片
        bullet_slide_layout = prs.slide_layouts[1]
        for item in content:
            slide = prs.slides.add_slide(bullet_slide_layout)
            title_shape = slide.shapes.title
            body_shape = slide.placeholders[1]
            title_shape.text = item.get("heading", "")
            text_frame = body_shape.text_frame
            text_frame.clear()
            p = text_frame.paragraphs[0]
            p.text = item.get("text", "")
            # 可选：本页配图（image_path 本地路径 或 image_url 下载后插入）
            raw_url = (item.get("image_url") or "").strip()
            if raw_url.startswith(("http://", "https://")):
                img_path = await asyncio.to_thread(_resolve_slide_image_path, item, output_dir)
            else:
                img_path = _resolve_slide_image_path(item, output_dir)
            if img_path:
                try:
                    # 固定宽度下按宽高比计算高度，避免长图被压扁；无 Pillow 时使用固定尺寸
                    pic_width_em = 3.5
                    pic_height_em = 2.6
                    try:
                        from PIL import Image
                        with Image.open(img_path) as im:
                            w, h = im.size
                            if w and h:
                                ratio = h / w
                                pic_height_em = min(pic_width_em * ratio, 5.0)
                    except Exception:
                        pass
                    slide.shapes.add_picture(
                        img_path,
                        Inches(6.2),
                        Inches(1.8),
                        width=Inches(pic_width_em),
                        height=Inches(pic_height_em),
                    )
                except Exception as img_err:
                    logger.warning("PPT 插入图片失败 %s: %s", img_path[:80], img_err)
        
        # 保存
        prs.save(output_path)
        
        return {
            "status": "success",
            "file": output_path,
            "slides": len(content) + 1,
            "message": f"PPT 文档已生成: {output_path}"
        }
    
    except Exception as e:
        logger.error(f"PPT 生成失败: {e}")
        return {
            "status": "error",
            "error": str(e),
            "message": f"PPT 生成失败: {e}"
        }


async def _generate_pdf_internal(
    title: str,
    content: str,
    output_path: str = "output.pdf",
) -> Dict[str, Any]:
    """
    内部实现：生成 PDF 文档
    
    Args:
        title: 文档标题
        content: 文档内容（纯文本或 HTML）
        output_path: 输出文件路径
    
    Returns:
        生成结果
    """
    output_path = _resolve_output_path(output_path)
    try:
        from reportlab.lib.pagesizes import letter
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer
        from reportlab.lib.units import inch
        
        # 创建 PDF 文档
        doc = SimpleDocTemplate(output_path, pagesize=letter)
        story = []
        
        # 样式
        styles = getSampleStyleSheet()
        title_style = ParagraphStyle(
            'CustomTitle',
            parent=styles['Heading1'],
            fontSize=18,
            textColor='#1f4788',
            spaceAfter=30,
            alignment=1  # 居中
        )
        
        # 添加标题
        story.append(Paragraph(title, title_style))
        story.append(Spacer(1, 0.3 * inch))
        
        # 添加内容
        body_style = styles['BodyText']
        for line in content.split('\n'):
            if line.strip():
                story.append(Paragraph(line, body_style))
                story.append(Spacer(1, 0.2 * inch))
        
        # 生成 PDF
        doc.build(story)
        
        return {
            "status": "success",
            "file": output_path,
            "size": os.path.getsize(output_path),
            "message": f"PDF 文档已生成: {output_path}"
        }
    
    except Exception as e:
        logger.error(f"PDF 生成失败: {e}")
        return {
            "status": "error",
            "error": str(e),
            "message": f"PDF 生成失败: {e}"
        }


async def _generate_word_internal(
    title: str,
    content: List[Dict[str, str]],
    output_path: str = "output.docx",
) -> Dict[str, Any]:
    """
    内部实现：生成 Word 文档
    
    Args:
        title: 文档标题
        content: 段落列表
                 格式: [{"heading": "...", "text": "..."}, ...]
        output_path: 输出文件路径
    
    Returns:
        生成结果
    """
    output_path = _resolve_output_path(output_path)
    try:
        # ✅ 检查依赖是否安装
        try:
            from docx import Document
            from docx.shared import Pt, RGBColor
            from docx.enum.text import WD_ALIGN_PARAGRAPH
        except ImportError as import_err:
            logger.warning(f"⚠️ python-docx 未安装: {import_err}")
            return {
                "status": "error",
                "error": f"python-docx 模块未安装。请运行: pip install python-docx",
                "message": f"Word 生成失败: python-docx 模块未安装。请运行: pip install python-docx"
            }
        
        # 创建 Word 文档
        doc = Document()
        
        # 添加标题
        title_para = doc.add_paragraph(title)
        title_para.style = 'Heading 1'
        title_para_format = title_para.paragraph_format
        title_para_format.alignment = WD_ALIGN_PARAGRAPH.CENTER
        
        # 添加内容
        for item in content:
            # 小标题
            if item.get("heading"):
                heading = doc.add_paragraph(item["heading"])
                heading.style = 'Heading 2'
            
            # 文本
            if item.get("text"):
                text = doc.add_paragraph(item["text"])
                text.style = 'Normal'
        
        # 保存
        doc.save(output_path)
        
        return {
            "status": "success",
            "file": output_path,
            "paragraphs": len(content) + 1,
            "message": f"Word 文档已生成: {output_path}"
        }
    
    except ImportError as import_err:
        logger.error(f"Word 生成失败 - 依赖缺失: {import_err}")
        return {
            "status": "error",
            "error": f"python-docx 模块未安装: {import_err}",
            "message": f"Word 生成失败: python-docx 模块未安装。请运行: pip install python-docx"
        }
    except Exception as e:
        logger.error(f"Word 生成失败: {e}")
        return {
            "status": "error",
            "error": str(e),
            "message": f"Word 生成失败: {e}"
        }


