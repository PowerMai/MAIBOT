def _resolve_output_path(output_path: str) -> str:
    """相对路径解析到工作区 outputs 目录，保证生成文件落入 output。"""
    p = Path(output_path)
    if not p.is_absolute():
        base = get_workspace_root() / "outputs"
        p = (base / output_path).resolve()
        p.parent.mkdir(parents=True, exist_ok=True)
    return str(p)


